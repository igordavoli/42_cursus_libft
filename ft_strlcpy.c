#include "libft.h"
#include <string.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	srclen;

	srclen = ft_strlen(src);
	ft_memmove(dst, src, dstsize);
	if (dstsize != 0)
		dst[dstsize - 1] = '\0';
	return (srclen);
}
